# ─── File I/O ────────────────────────────────────────────────────────────────

def load_api(file_key: str) -> list[dict]:
    path = FILES[file_key]
    if not path.exists():
        print(f"Error: Working file not found: {path}", file=sys.stderr)
        print("Copy the originals first:", file=sys.stderr)
        print(f"  cp server/GSCode.NET/api/t7_api_gsc.json .tmp/t7_api_gsc_working.json", file=sys.stderr)
        sys.exit(1)
    with open(path, encoding="utf-8") as f:
        return json.load(f)["api"]


def save_api(file_key: str, entries: list[dict]):
    path = FILES[file_key]
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        json.dump({"api": entries}, f, indent=4, ensure_ascii=False)
        f.write("\n")


def load_original(file_key: str) -> list[dict]:
    path = ORIGINALS[file_key]
    if not path.exists():
        return []
    with open(path, encoding="utf-8") as f:
        return json.load(f)["api"]


def find_entry(entries: list[dict], name: str) -> tuple[int, dict] | None:
    name_lower = name.lower()
    for i, entry in enumerate(entries):
        if entry["name"].lower() == name_lower:
            return i, entry
    return None


def get_flags(entry: dict) -> set[str]:
    return set(entry.get("flags", []))


def get_state(entry: dict) -> str:
    flags = get_flags(entry)
    if "verified" in flags:
        return "verified"
    if PROCESSED_FLAG in flags:
        return "processed"
    if "autoprocessed" in flags:
        return "autoprocessed"
    if "autogenerated" in flags:
        return "autogenerated"
    return "unknown"


# ─── Linting ─────────────────────────────────────────────────────────────────

def is_pascal_case(name: str) -> bool:
    """Check if name is PascalCase. Underscores are allowed as segment separators,
    but each segment must start with an uppercase letter (e.g. Bb_GetArrivalStance)."""
    if not name:
        return False
    if " " in name:
        return False
    segments = name.split("_") if "_" in name else [name]
    for seg in segments:
        if not seg or not seg[0].isupper():
            return False
    return True


def is_camel_case(name: str) -> bool:
    """Check if name is camelCase (first char lowercase, no spaces)."""
    if not name:
        return False
    if " " in name:
        return False
    if not name[0].islower():
        return False
    return True


def is_generic_param_name(name: str) -> bool:
    """Check if param name is a generic placeholder like arg0, arg1, etc."""
    return bool(re.match(r"^arg\d+$", name))


def find_british_spellings(text: str) -> list[tuple[str, str]]:
    """Find British English spellings in text, return (british, american) pairs."""
    if not text:
        return []
    findings = []
    words = re.findall(r"[a-zA-Z]+", text.lower())
    for word in words:
        if word in BRITISH_TO_AMERICAN:
            findings.append((word, BRITISH_TO_AMERICAN[word]))
    return findings


def lint_entry(entry: dict) -> list[str]:
    """Lint a single API entry, return list of issues."""
    issues = []
    name = entry.get("name", "")
    state = get_state(entry)

    # Function name: PascalCase
    if not is_pascal_case(name):
        issues.append(f"Function name '{name}' is not PascalCase")

    # Description: non-empty, ends with period
    desc = entry.get("description", "")
    if not desc or not desc.strip():
        issues.append("Missing function description")
    elif not desc.strip().endswith("."):
        issues.append(f"Description does not end with a period: \"{desc}\"")

    # British spellings in description
    british = find_british_spellings(desc)
    for brit, amer in british:
        issues.append(f"British spelling '{brit}' in description — use '{amer}'")

    # Confidence field (required for processed)
    if state == "processed":
        conf = entry.get("confidence")
        if conf not in VALID_CONFIDENCES:
            issues.append(f"Processed entry missing valid confidence (got '{conf}')")

    # Overloads
    for ov_idx, overload in enumerate(entry.get("overloads", [])):
        ov_prefix = f"Overload {ov_idx + 1}: " if len(entry.get("overloads", [])) > 1 else ""

        # CalledOn
        called_on = overload.get("calledOn")
        if called_on and isinstance(called_on, dict):
            co_name = called_on.get("name", "")
            co_desc = called_on.get("description")
            if co_name and not is_camel_case(co_name):
                issues.append(f"{ov_prefix}CalledOn name '{co_name}' is not camelCase")
            if not co_desc:
                issues.append(f"{ov_prefix}CalledOn '{co_name}' missing description")
            elif not co_desc.strip().endswith("."):
                issues.append(f"{ov_prefix}CalledOn description does not end with period")
            british = find_british_spellings(co_desc or "")
            for brit, amer in british:
                issues.append(f"{ov_prefix}CalledOn: British spelling '{brit}' — use '{amer}'")

        # Parameters
        for p_idx, param in enumerate(overload.get("parameters", [])):
            p_name = param.get("name", "")
            p_desc = param.get("description", "")
            p_prefix = f"{ov_prefix}Param '{p_name}'"

            # Name checks
            if is_generic_param_name(p_name):
                issues.append(f"{p_prefix}: generic placeholder name")
            elif not is_camel_case(p_name):
                issues.append(f"{p_prefix}: not camelCase")

            # Description checks
            if not p_desc or not p_desc.strip():
                issues.append(f"{p_prefix}: missing description")
            elif not p_desc.strip().endswith("."):
                issues.append(f"{p_prefix}: description does not end with period")

            # British spellings
            british = find_british_spellings(p_desc)
            for brit, amer in british:
                issues.append(f"{p_prefix}: British spelling '{brit}' — use '{amer}'")

        # Returns
        returns = overload.get("returns")
        if returns and not returns.get("void", False):
            ret_type = returns.get("type")
            if not ret_type:
                issues.append(f"{ov_prefix}Non-void return but missing return type")

    # Example
    example = entry.get("example", "")
    if not example or not example.strip():
        issues.append("Missing example")

    return issues


