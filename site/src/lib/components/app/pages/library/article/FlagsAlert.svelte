<script lang="ts">
	import { Button } from '$components/ui/button';
	import * as Alert from '$lib/components/ui/alert/index.js';
	// @ts-ignore
	import BadgeCheck from "lucide-svelte/icons/badge-check";
	// @ts-ignore
	import EyeOff from "lucide-svelte/icons/eye-off";
	// @ts-ignore
	import MessageCircleQuestion from "lucide-svelte/icons/message-circle-question";
	// @ts-ignore
	import BookOpenText from "lucide-svelte/icons/book-open-text";
	// @ts-ignore
	import SquareCode from "lucide-svelte/icons/square-code";
	import FlagAlert from './FlagAlert.svelte';
	import * as Tooltip from '$lib/components/ui/tooltip/index.js';

	interface Props {
		flags: string[];
		confidence?: string | null;
	}

	let { flags = [], confidence = null }: Props = $props();

	const confidenceLabels: Record<string, { label: string; meaning: string }> = {
		high: { label: 'High', meaning: 'Near certainty that parameters are correctly typed and fully documented.' },
		medium: { label: 'Medium', meaning: 'Reasonably certain of correctness, but there may be undocumented quirks.' },
		low: { label: 'Low', meaning: 'Best-guess based on limited information. May be inaccurate.' }
	};
</script>

{#if flags.includes('autogenerated')}
	<FlagAlert Icon={MessageCircleQuestion} title={"Unverified"} description={`
		This function's documentation was auto-generated from Treyarch's API, which is known to
		contain errors.
	`}/>
{/if}

{#if flags.includes('processed')}
	{@const conf = confidence && confidenceLabels[confidence]}
	<Alert.Root class={'[&:has(svg)]:pl-12'}>
		<BookOpenText class="w-6 h-6" />
		<Alert.Title class="text-sm lg:text-base">Generated</Alert.Title>
		<Alert.Description class="flex flex-col gap-2 items-start text-xs lg:text-sm">
			This function's documentation was batch-processed and may contain inaccuracies.
			{#if conf}
				<span class="inline-flex items-center gap-1">
					Confidence:
					<Tooltip.Provider>
						<Tooltip.Root>
							<Tooltip.Trigger>
								<strong class="underline decoration-dotted cursor-help">{conf.label}</strong>
							</Tooltip.Trigger>
							<Tooltip.Content>
								<p class="max-w-xs text-xs">{conf.meaning}</p>
							</Tooltip.Content>
						</Tooltip.Root>
					</Tooltip.Provider>
				</span>
			{/if}
		</Alert.Description>
	</Alert.Root>
{/if}

{#if flags.includes('verified')}
	<FlagAlert Icon={BadgeCheck} title={"Community contribution"} description={`
		This function's documentation has been verified or corrected by a member of the community.
	`}/>
{/if}

{#if flags.includes('unlisted')}
	<FlagAlert Icon={EyeOff} title={"Unlisted"} description={`
		This function has been unlisted. It is unlikely to be functional and/or useful.
	`}/>
{/if}

{#if flags.includes('devblock')}
	<FlagAlert Icon={SquareCode} title={"Dev Block"} description={`
		This function is only available inside developer blocks.
	`}/>
{/if}
